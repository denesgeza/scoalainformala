# Să se scrie o clasă Fracție(numărător, numitor) care sa implementeze următoarele metode:
# ○ __init__ : instanțiem numărător și numitor
# ○ __str__ : afișăm "numărător/numitor"
# ○ __add__ : returnam o nouă fracție care reprezinta adunarea
# ○ __sub__: returnam o nouă fracție care reprezinta scăderea
# ○ inverse: returnează o nouă fracție (inversa fracției)